# Шаг 1: Удалить все пробелы и привести строку к нижнему регистру
# Шаг 2: Создать переменную для хранения строки в обратном порядке
# Шаг 3: Сравнить исходную строку с обратной строкой
# Если они равны, то строка является палиндромом, иначе - нет


def is_palindrome(s):
    # Шаг 1: Удалить все пробелы и привести строку к нижнему регистру
    s = s.replace(" ", "").lower()

    # Шаг 2: Создать переменную для хранения строки в обратном порядке
    reversed_s = s[::-1]

    # Шаг 3: Сравнить исходную строку с обратной строкой
    return s == reversed_s


print(is_palindrome("radar"))  # True
print(is_palindrome("hello"))  # False